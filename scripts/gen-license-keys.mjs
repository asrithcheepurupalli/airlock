// Generates the Ed25519 keypair for Airlock Pro licenses. Run ONCE.
//   node scripts/gen-license-keys.mjs
//
//   PUBLIC key  -> extension/license-pubkey.js  (committed; the extension verifies offline with this)
//   PRIVATE key -> .license-private-key          (gitignored; paste into Vercel env LICENSE_PRIVATE_KEY)
//
// The private key value is never printed to the console, only written to the
// gitignored file. Copy it into Vercel, then you may delete the file.
import crypto from 'node:crypto'
import { writeFileSync, existsSync } from 'node:fs'

const pubPath = new URL('../extension/license-pubkey.js', import.meta.url)
const privPath = new URL('../.license-private-key', import.meta.url)

if (existsSync(privPath) && !process.argv.includes('--force')) {
  console.error('.license-private-key already exists. Refusing to overwrite (would invalidate every issued license).')
  console.error('Pass --force only if you really mean to rotate keys.')
  process.exit(1)
}

const { publicKey, privateKey } = crypto.generateKeyPairSync('ed25519')
const pubX = publicKey.export({ format: 'jwk' }).x // base64url raw 32-byte public key
const privB64 = privateKey.export({ format: 'der', type: 'pkcs8' }).toString('base64')

writeFileSync(
  pubPath,
  '// AUTO-GENERATED by scripts/gen-license-keys.mjs. Public key only, safe to commit.\n' +
    '// The extension verifies Pro licenses against this, fully offline.\n' +
    `window.AIRLOCK_LICENSE_PUBKEY = ${JSON.stringify(pubX)}\n`
)
writeFileSync(privPath, privB64 + '\n')

console.log('Wrote extension/license-pubkey.js  (public key, commit this)')
console.log('Wrote .license-private-key         (PRIVATE, gitignored)')
console.log('')
console.log('Next: copy the contents of .license-private-key into the Vercel env var')
console.log('LICENSE_PRIVATE_KEY, then delete the file. Never commit it.')
